  關於退貨運費
  Q15：退貨退款訂單的相關運費該如何支付呢？

  A：退貨運費的處理於商城賣家和優選 / 一般賣家有所不同：

  商城賣家：若退貨退款成立，且賣家提出爭議未果，方會於次月月初10日前統整上月所產生的相關運費從賣家本網站錢包扣除，同時賣家可於次月25日前向本網站申請相關補貼。

